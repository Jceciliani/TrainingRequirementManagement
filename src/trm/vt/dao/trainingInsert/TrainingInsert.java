package trm.vt.dao.trainingInsert;

public class TrainingInsert {

	private int employee_id;
	private int training_session_id;
	private String first_name;
	private String last_name;
	private String email;
	private String phone_number;
	
	
	public int getEmployee_id() {
		return employee_id;
	}
	public void setEmployee_id(int employee_id) {
		this.employee_id = employee_id;
	}
	public int getTraining_session_id() {
		return training_session_id;
	}
	public void setTraining_session_id(int training_session_id) {
		this.training_session_id = training_session_id;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone_number() {
		return phone_number;
	}
	public void setPhone_number(String phone_number) {
		this.phone_number = phone_number;
	}
	
	
}
